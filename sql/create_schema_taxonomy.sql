	COMMENT ON COLUMN taxonomy.taxa_reference.authors IS 'The name of the authors who published the taxon';
	COMMENT ON COLUMN taxonomy.taxa_reference.id_rank IS 'The taxonomic rank of the taxon';
	COMMENT ON COLUMN taxonomy.taxa_reference.id_parent IS 'The hierarchical parent of the taxon';
